  alias TakeaplegeApi.App.Session

  @doc """
  Returns the list of sessions.

  ## Examples

      iex> list_sessions()
      [%Session{}, ...]

  """
  def list_sessions do
    Repo.all(Session)
  end

  @doc """
  Gets a single session.

  Raises `Ecto.NoResultsError` if the Session does not exist.

  ## Examples

      iex> get_session!(123)
      %Session{}

      iex> get_session!(456)
      ** (Ecto.NoResultsError)

  """
  def get_session!(id), do: Repo.get!(Session, id)

  @doc """
  Gets a single session by token.

  Raises `Ecto.NoResultsError` if the Session does not exist.

  ## Examples

      iex> get_session_by_token!("random")
      %Session{token: "random", user_id: 1, id: 10}

      iex> get_session_by_token!("none")
      ** (Ecto.NoResultsError)

  """
  def get_session_by_token!(token), do: Repo.get_by!(Session, token: token)

  @doc """
  Creates a session.

  ## Examples

      iex> create_session(%{field: value})
      {:ok, %Session{}}

      iex> create_session(%{field: bad_value})
      {:error, %Ecto.Changeset{}}

  """
  def create_session(user_params \\ %{}) do
    import Comeonin.Bcrypt, only: [checkpw: 2, dummy_checkpw: 0]
    
    user = Repo.get_by(User, email: "#{user_params["email"]}")
    cond do
      user && checkpw(user_params["password"], user.password_hash) ->
        find_or_create_session(user.id)
      user ->
        {:error, user_params}
      true ->
        dummy_checkpw()
        {:error, %User{}}
    end
  end

  @doc """
  Find or Create Session

  ## Examples

    iex> find_or_create_session(session, %{field: new_value})
    {:ok, %Session{}}
  """
  def find_or_create_session(user_id) do
    session = Repo.get_by(Session, user_id: user_id)
    cond do
      session -> {:ok, %Session{} = session}
      nil || true ->
        %Session{}
        |> Session.registration_changeset(%{user_id: user_id})
        |> Repo.insert()
    end
  end

  @doc """
  Updates a session.

  ## Examples

      iex> update_session(session, %{field: new_value})
      {:ok, %Session{}}

      iex> update_session(session, %{field: bad_value})
      {:error, %Ecto.Changeset{}}

  """
  def update_session(%Session{} = session, attrs) do
    session
    |> Session.changeset(attrs)
    |> Repo.update()
  end

  @doc """
  Deletes a Session.

  ## Examples

      iex> delete_session(session)
      {:ok, %Session{}}

      iex> delete_session(session)
      {:error, %Ecto.Changeset{}}

  """
  def delete_session(%Session{} = session) do
    Repo.delete(session)
  end

  @doc """
